public class ConstDemo {

	public static void main(String[] args) {
		System.out.println("Hello world");//字符串常量
		System.out.println(123);//整数常量
		System.out.println(-223);
		System.out.println(123.3);//小数常量
		System.out.println('中');//字符常量
		System.out.println('a');//字符常量
		System.out.println(true);//bool常量
		System.out.println(false);
		
		System.out.println(0b0100);//2进制
		System.out.println(0100);//8进制
		System.out.println(100);//10进制
		System.out.println(0x0100);//16进制
		System.out.println("hhhah");
	}

}
